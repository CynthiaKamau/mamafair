            <h4>Other general principles</h4>

            <p> In uncomplicated pregnancies, midwife/GP care should normally be offered, with specialist care readily available when complications occur.</p>
            <p>The patient should be seen by a small group of professionals who provide continuity of care.</p>
            <p> Antenatal care should be readily and easily accessible and should be in an environment which enables women to discuss confidential issues such as domestic violence, sexually transmitted infections, mental health problems or recreational drug use.</p>
   <p> Assessment of gestational age should be based on an early ultrasound scan rather than the last menstrual period. Such scans should be offered to all women between 10 and 13 weeks and help to ensure:
       <ul>
           <li> Consistency of gestational age assessments.</li>
           <li>Multiple pregnancy is picked up early.</li>
           <li> Improved accuracy of Down's screening assessment.</li>
           <li>Sensible decisions on induction of labour after 41 weeks.</li>
            </ul>
            
            <p>At 38 weeks, she should be given:

    Information about management options for prolonged pregnancy.</p>



                            <h4 id="PE0">  Mental Health </h4>

<p>Be alert to mental health problems - current or potential. The National Institute for Health and Care Excellence (NICE) updated its advice on mental health in pregnancy and the postnatal period on 2014. Guidance advises healthcare professionals, at a woman's first contact with primary care, her booking visit and during the early postnatal period, to:
    <ul>

        <li> Consider asking questions to screen for depression and anxiety as part of a general discussion about a woman's mental health and well-being.</li>
        <li>Ask about any past or present severe mental illness.</li>
        <li> Ask about past or present treatment by a specialist mental health service, including inpatient care</li>
        <li> Ask about any severe perinatal mental illness in a first-degree relative.</li>
            </ul>
<p>In women with established mental health issues, consider the needs of partners, families and carers that might affect a woman with a mental health problem in pregnancy and the postnatal period. These include:
    <ul>

        <li>The welfare of the baby and other dependent children and adults.</li>
        <li> The role of the partner, family or carer in providing support.</li>
        <li> The potential effect of any mental health problem on the woman's relationship with her partner, family or carer.</li>
            </ul>
        </p>

     <h4 id="PE1"> Advice on Employment </h4>

<p>Advise on maternity rights and benefits. Pregnant women have the right to:
    <ul>
        <li> Paid time off for antenatal care and classes.</li>
        <li>Maternity leave.</li>
        <li> Maternity pay or maternity allowance.</li>
        <li>Protection against unfair treatment, discrimination or dismissal. Contracts cannot be changed without agreement.</li>
                </ul>

<p>Note that Maternity Leave and Statutory Maternity Pay will start automatically if the employee is off work for a pregnancy-related illness in the four weeks before the baby is due.</p>

<p>For most women, it is safe to continue working in pregnancy, so reassure where appropriate. However, ask about occupation and consider potential exposure to harmful agents. It is an employer's responsibility to assess risk when informed of a woman's pregnancy. If a risk is established, it is their responsibility to find alternative duties or suspend on full pay. Further information for expectant mothers and for employers is available on the Health and Safety Executive website.</p>

    <p>The maternity certificate MAT B1 form should be signed to enable a pregnant woman to claim Statutory Maternity Pay (SMP) from her employer or Maternity Allowance (MA) from Jobcentre Plus.[6]It can be signed from 20 weeks before the estimated date of delivery (EDD) and doctors or midwives are required to sign this free of charge.</p>
            
     <h4 id="PE2">  Advice on Nutrition </h4>

<p>Pregnant women should be encouraged to have a normal, balanced, healthy diet. Because of the dangers of toxoplasmosis and listeriosis, women should avoid:
                <ul>
                <li> Uncooked meat or fish.</li>
                <li> Raw or partially cooked eggs, and products such as fresh mayonnaise which may contain raw eggs.</li>
                <li> Milk that has not been pasteurised.</li>
                <li> Soft cheeses which are mould-ripened or made from unpasteurised milk, such as Brie and Camembert, or soft blue-veined cheeses such as Danish Blue.</li>
                <li>Pâté of any sort.</li>
                <li>Raw shellfish.</li>
                <li> Shark, swordfish and marlin (due to high mercury levels).</li>
                <li>More than two portions per week of oily fish.</li>
                <li>Unwashed fruit or vegetables. (Fruit and vegetables should be washed due to the small risk of toxoplasma from soil.)</li>
              </ul>

<p>Vegetarians, and especially vegans, may be at risk of nutritional deficiencies and may need to be referred to a dietician for advice about obtaining all nutrients through diet.

Women should be cautioned to avoid many herbal preparations and teas; their use and safety in pregnancy have not been studied.</p>
                
 <h4 id="PE3"> Advice on Supplements </h4>
                <ul>
                    <li>Folic acid</li>
                </ul>

<p>All women intending to become pregnant, and those who are, should be advised to take 400 micrograms of folic acid up to 12 weeks of gestation to reduce incidence of fetal neural tube defects (NTDs). Supplementation with folic acid is one of the most significant interventions available. 400 micrograms/day for all women have been shown consistently to reduce the incidence of NTDs, such as spina bifida, significantly.</p>

<p>Women who have already given birth to a child with an NTD, or have a family history of NTD or who are at a higher risk, should be prescribed 5 mg/day. This dose is also recommended for women with diabetes (types 1 or 2).</p>

<p>This is given for at least one month prior to conception and three months afterwards. Diet alone does not reliably supply an adequate amount of folic acid for this effect. It can be prescribed or bought over-the-counter (OTC) cheaply.</p>
                <ul>
                    <li>Vitamin D</li>
                </ul>

Adequate vitamin D stores during pregnancy and breast-feeding are important for the health of both mother and baby. All women should be advised to take vitamin D supplements (10 micrograms = 400 units per day). The following groups are at higher risk and should take 1,000 units per day:
<ul>
    <li>Housebound women or those who have limited exposure to sunlight, such as women who usually remain covered when outdoors.</li>
    <li> South Asian, African, Caribbean or Middle Eastern family origin.</li>
    <li> Women with a pre-pregnancy BMI more than 30 kg/m2.</li>
    <li>Women with a high risk of pre-eclampsia (should take at least 800 units per day combined with calcium.)</li>
</ul>
Caution with vitamin D supplementation is needed in women with sarcoidosis or renal disease.

    <p> <ul><li>Iron</li></ul>
    

Iron should not be offered routinely as it has no benefit to either mother or baby and may cause constipation and other side-effects. Women should be given dietary advice, encouraging dietary intake of iron. Where supplementation is required, this will be picked up on routine blood tests at booking and 28 weeks.

            <p>Anaemia is defined in pregnancy as:</p>
            <ul>

                <li> Hb less than 110 g/L in the first trimester.</li>
                <li> Hb less than 105 g/L in the second and third trimesters.</li>
                <li> Hb less than 100 g/L postpartum.</li>
            </ul>
If established, it should be treated with 100-200 mg of oral elemental iron per day for three months and for at least six weeks postpartum.

                <p>  <ul><li>Other vitamins</li></ul>

<p>Women should be warned that high levels of vitamin A may be teratogenic and therefore that they should avoid extra supplementation. Liver and liver products may contain high levels of vitamin A and should be avoided. Where using multivitamin preparations, women should use those which are specifically designed for pregnancy. Pregnant women on low incomes can obtain vouchers for vitamins from the Healthy Start programme in the UK.</p>
                <h4> Advice on medication</h4>

<p>Advise women to use as few medicines as possible during pregnancy and only when benefit outweighs risk. This includes OTC medication and complementary therapies, as few products have been shown to be definitely safe during pregnancy. Ideally review any regular medication pre-conception but, if this has not been done, as soon as possible in pregnancy.</p>
                
                 <h4 id="PE4">  Advice on Lifestyle </h4>
                <ul>
                    <li> Exercise</li></ul>

<p>Women who exercise regularly should be advised to continue to do so. Those who are inactive should start a gentle programme of regular exercise. Moderate exercise has not been shown to cause any harm but the patient should be warned of the dangers of highly energetic and contact sports that would risk damage to the abdomen, falls or excessive joint stress.</p>

<p>Scuba diving should be avoided, as it can cause fetal birth defects and fetal decompression disease.</p>
                <p><ul>
                <li>Sexual intercourse</li></ul></p>

<p>This has not been shown to cause any harm during pregnancy. It may be advisable to avoid it if there is risk of preterm rupture of membranes or if there is placenta praevia, although evidence is limited.</p>
                <ul><li>Alcohol</li></ul>

<p>High levels of alcohol consumption during pregnancy may result in the fetal alcohol syndrome (FAS). There are various components including growth restriction, general learning disability, facial anomalies and behavioural problems. Not all women who drink heavily in pregnancy have babies with FAS, so there are other components, which are as yet poorly understood.</p>

<p>It is not known how much alcohol is safe to drink in pregnancy. Current advice from the Royal College of Obstetricians and Gynaecologists (RCOG), NICE and the British Medical Association (BMA) is that women should not drink alcohol at all in the first three months of pregnancy. (This is because of the increased risk of miscarriage.) For the rest of pregnancy, women are advised ideally not to drink at all; however, if they do, to restrict it to a maximum of one or two units at a maximum of once or twice per week.</p>
   <ul><li>             
       Smoking</li></ul>

<p>Smoking in pregnancy is associated with a large number of adverse effects in pregnancy including:
    <ul>
        <li>Intrauterine growth restriction and low birth weight.</li>
        <li> Miscarriage and stillbirth.</li>
        <li> Premature delivery.</li>
        <li> Placental problems.</li></ul>
                

<p>There is evidence that quitting smoking early reduces risks.NICE guidelines encourage GPs and midwives to be proactive in helping with this. Women should be asked about smoking at their first contact and at each subsequent appointment if they are smokers. Offer referral to NHS smoking services and give them the number of the NHS Smoking Helpline (0800 123 1044). Information about the risks of smoking to the fetus and the mother and then of secondhand smoke to a newborn baby should be given.</p>

<p>There is little information on the use of nicotine replacement therapy (NRT) in pregnancy but smoking gives a greater dose of nicotine and also exposes mother and fetus to other toxins. It is likely to be safer than smoking in mothers for whom non-pharmacological interventions have failed. If a woman expresses a clear wish to receive NRT, use professional judgement when deciding whether to offer a prescription. Advise pregnant women using nicotine patches to remove them before going to bed.</p>

Neither bupropion nor varenicline should be prescribed in pregnancy.
               <p> <ul><li>
                Recreational drug use</li></ul></p>

<p>The number of women misusing drugs has increased considerably in the past few decades and many are in their child-bearing years. Though pregnancy may act as a catalyst for change and present a "window of opportunity", drug misusers may not use general health services until late into pregnancy and this increases the health risks for both the mother and child.</p>

   <p> A multidisciplinary approach is essential. Most localities will have a clearly defined drug dependency service with a readily accessible entry point.</p>
   <p> Cocaine use in pregnancy is particularly serious and there is no substitute. It has been associated with spontaneous abortion, placental abruption, premature birth, low birth weight and sudden infant death syndrome. There is conflicting evidence regarding fetal abnormalities.</p>
   <p> Opiate use is associated with increased incidence of intrauterine growth restriction and preterm delivery. This contributes to an increased rate of low birth weight and perinatal mortality. Maintenance treatment with an opiate substitute improves outcomes.</p>
    <p>Women addicted to heroin who wish to become pregnant should be urged to enter a detoxification programme before conception and, if not, then at least be stabilised on methadone. Buprenorphine is also used as a substitute with apparently similar outcomes in pregnancy.</p>
    <p>HIV and hepatitis B screening is carried out in all pregnant women in the UK but is especially important in known IV drug users. Hepatitis C screening is also recommended in this group.</p>
   <p> The use of cannabis may be harmful to the fetus and is also associated with smoking and should be discouraged.
       Cochrane reviews have found no convincing evidence for psychosocial interventions.</p>

            <p> <ul><li>Travel</li></ul></p>

<p>Flying is associated with increased risk of deep vein thrombosis (DVT) but it is not known if the risk is further increased in pregnancy. Use of compression hosiery reduces the risk in the general population. Also discuss vaccinations and travel insurance if travelling abroad. There is no evidence that flying is associated with an increased risk of miscarriage or premature labour. Airlines have their own guidance about from what gestation pregnant women should not fly for practical reasons.

Advise on proper use of seat belts for car travel, with belts above and below bump rather than over it.</p>
    <p><ul><li><b>Treating symptoms of early pregnancy</b></li></ul></p>

<p>Nausea and vomiting of pregnancy generally resolve by 12-20 weeks of gestation. Cochrane reviews have failed to find evidence of efficacy and safety for any of the many treatments used.[21]There was little evidence found for the efficacy of acupuncture. Ginger, anti-emetics and antihistamines are often used.</p>

<p>Heartburn may be alleviated by taking small meals and raising the head of the bed. It may need antacids. The manufacturers of Gaviscon® state it is safe for use in pregnancy.</p>

<p>Constipation is another common symptom in early pregnancy. Women should be advised on diet to combat this (fluids and increasing dietary fibre) or may use bran or wheat fibre supplementation.</p>
    
                 <h5 id="PE5"> <b>Advice on Appointments and Doctor's visits</b></h5>

                <p>The following number of appointments is generally recommended in uncomplicated pregnancies:</p>
<ul>
    <li> Nulliparous women - 10</li>
    <li> Parous women - 7</li></ul>

<p>Information about the timing and function of these appointments should be given to the woman in writing with the chance to discuss them with her doctor or midwife. Appointments should have a focus and structure and include routine tests where possible.</p>

<p>All appointments should include measuring BP and testing urine for proteinuria. Pre-eclampsia occurs more frequently in:</p>
<ul>
    <li> Those with a previous history.</li>
    <li>The nulliparous.</li>
    <li> Multiple pregnancies.</li>
    <li>Those aged over 40.</li>
    <li>Those with a close family history.</li>
    <li> Those where BMI is over 35 at first presentation.</li>
    <li> Those with pre-existing vascular disease - eg, hypertension.</li>
    <li>Those with pre-existing renal disease.</li>
    <li>Those with pre-existing diabetes.</li></ul>

<p>They should also be used as an opportunity to give information and allow the patient to ask questions and discuss any topics which are of concern to her. Domestic violence is a subject that pregnant women should be encouraged to discuss openly.</p>
                <b> First (booking) appointment</b>

                <p>This should be before 12 weeks of pregnancy, ideally by week 10. There may need to be two appointments because of the volume of information required to be imparted. All information should initially be offered verbally and backed up in writing with an opportunity to discuss and ask questions.</p>

                <p> Lifestyle topics such as diet, alcohol, smoking, exercises, etc,  together with antenatal care services available and maternity benefits should be covered. Initial measurement of weight, BMI and BP. Repeated weighing is only appropriate in later pregnancy where it is likely to affect management.</p>
                <p>The clinician needs to provide enough information to make an informed decision about undergoing available screening tests.</p>
                
    Offer screening of mother for:
                <ul>
                    <li> Anaemia.</li>
                    <li> Red cell allo-antibodies.</li>
                    <li> Hepatitis B virus.</li>
                    <li> HIV.</li>
                    <li>Rubella susceptibility.</li>
                    <li>Syphilis.</li>
                    <li>Chlamydia for women under the age of 25 via the chlamydia national screening programme.</li>
                    <li>Asymptomatic bacteriuria.</li>
                    <li>Sickle cell and thalassaemia screening (offer to all women, using the national Family Origin Questionnaire to establish level of risk).</li></ul>
        <p>Screening for gestational diabetes in women with risk factors:
            <ul>
                <li> BMI less than 30.</li>
                <li>Previous gestational diabetes.</li>
                <li> Previous baby with a weight equal or more than 4.5 kg.</li>
                <li>First-degree relative with diabetes.</li>
                <li>Afro-Caribbean or South Asian or Middle Eastern origin.</li></ul>
                Arrange as agreed.
    <p>Offer screening of fetus for chromosomal abnormalities:
            <p> Down's syndrome, Edwards' syndrome and Patau's syndrome.</p>

                <p> NB: Ensure the patient is aware that she is not obliged to have these screening tests and that they are not 100% reliable. Screening in the first trimester will be by the combined screening test (this involves a nuchal translucency measurement via scan and serum tests). The combined screening test can take place between 10 and 14 weeks of gestation. If a woman books too late for the combined screening test, she can be offered the quadruple serum screening test up until 20 weeks of gestation.</p>
   <p> All women should be offered screening of foetus for other structural anomalies by ultrasound scan at 18-20 weeks.
    It is necessary to identify those women who may require extra care and create a plan for this. Identify those with increased risk of pre-eclampsia. Ask about any current or previous significant medical or psychiatric illnesses.</p>

