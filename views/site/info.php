                <h2>POST NATAL INFORMATION </h2>
            <p>
            The puerperium covers the six-week period following birth, during which time the various changes that occurred during pregnancy revert to the non-pregnant state. Physiological changes during this time include:
<ul>
    <li>The cardiovascular system reverts to normal during the first two weeks. The extra load on the heart from the extra volume of blood disappears by the second week.</li>
    <li>The vaginal wall is initially swollen, bluish and pouting but rapidly regains its tone, although remaining fragile for 1-2 weeks. Perineal oedema may persist for some days.</li>
    <li>>After delivery of the placenta, the uterus is the size of a 20-week pregnancy; however, it reduces in size on abdominal examination by one finger-breadth each day, such that on the 12th day it cannot be palpated. By the end of the puerperium it is only slightly larger than pre-pregnancy.</li>
    <li>For the first 3-4 days, lochia comprises mainly blood and remnants of trophoblastic tissue. During days 3-12 the colour is reddish-brown but then changes to yellow. Occasionally, lochia may become red again for a few days, due to thrombi from the ends of vessels.</li></ul>

            <p> <h5 id="PN0"> Common Puerperal problems</h5>
<p>
    Perineum</p>

   <p> If the perineum has been damaged and repaired it may cause considerable pain, requiring analgesia. Women may prefer to sit on a rubber ring.
       If the perineum is painful, it is important to check the sutures and check for any signs of infection. Occasionally, sutures may need to be removed.</p>

            <p>Micturition</p>

    <p>Retention of urine can occur (possibly secondary to pudendal nerve bruising) and can occasionally require catheterisation[1].
        Approximately 50% of women will develop some urinary incontinence and this usually takes the form of stress incontinence. This may persist after the pregnancy. Pelvic floor exercises should be taught and encouraged</p>

            <p>Bowel problems</p>

            <p>constipation may be a problem for a short time and stool softeners may be useful.
                Haemorrhoids may be more painful after the birth than before. These can occasionally appear for the first time perinatally and normally disappear within a few weeks.</p>

            <p>Mastitis</p>

    <p>Mastitis may be due to failure to express milk from one part of the breast. It can be treated by ensuring all milk is expressed, feeding on the affected side first so this side is emptied most effectively.
    It may be complicated by infection with Staphylococcus aureus and require treatment with flucloxacillin.
        Very occasionally, a breast abscess develops and requires incision and drainage.</p>

            <p>Dyspareunia</p>

    <p>Ask about resumption of intercourse 2-6 weeks following delivery; examination of the perineum may be appropriate in women with dyspareunia who had perineal trauma.
        Discomfort during intercourse, especially in a woman who is breast-feeding, may be helped by using lubricant.</p>

            <p>Backache</p>

   <p> This may persist after the birth and affects approximately a third of women.
       Pain may be considerable and last for several months - occasionally much longer.</p>

            <p>Anaemia</p>
This is common and may easily be overlooked.

            <p>Psychological problems</p>
<p>'Baby blues': on days 3-5, a large proportion of women become temporarily sad, anxious, irritable and emotional:

    The precise cause of this is unknown and may involve hormonal changes, reaction to the reality of motherhood and doubts by the mother about her ability to care for the child.
    Management consists of an explanation and reassurance; the feelings should go within a few days.
    If symptoms do not resolve, the woman should be assessed for postnatal depression.</p>

            <p> <H5 id="PN1">Serious maternal health problems</H5>
            
            <p>Mental health problems</p>

   <p> Women are at their highest risk of having severe mental illness when pregnant and soon afterwards than at any other time in their lives.
       In the five-year period between 2009-2013, there were 101 late maternal deaths due to suicide in the UK (2.3 per 100,000 maternities) and 58 as a result of substance misuse</p>
    <p>Screening questions for depression and anxiety should be considered in the early postnatal period
        Around 10-15% of women experience postnatal depression which may present at any time during the first year after delivery.</p>
            <p> Postpartum psychosis:</p>
            <p>This affects 1-2/1,000 women following delivery and usually appears as mania or depression but women sometimes present with apparent schizophrenia</p>
            <p>It usually begins abruptly at 5-15 days, initially with confusion, anxiety, restlessness and sadness.</p>
            <p>There is rapid development of delusions - eg, the baby has died or is deformed - or hallucinations with deepening melancholia.
                Any woman with symptoms suggestive of postpartum psychosis should be referred to a secondary mental health service for assessment within four hours. Admission to hospital is usual and ideally this should be to a specialist mother and baby unit.</p>

            <p>Substance misuse:</p>
       <p> In the five-year period between 2009-2013, 58 women in the UK died as a result of substance misuse (alcohol and/or drugs) either during their pregnancy or up to one year later
        Clinicians need to ensure that their attitude does not prevent women who misuse substances from accessing the care and support that they need
           A recent significant change in mental state or new symptoms, new thoughts or acts of self-harm or new and persistent expressions of inadequacy as a mother are all 'red flags' for urgent senior psychiatric assessment.</p>

            <p>Postpartum haemorrhage</p>

   <p> Primary postpartum haemorrhage is defined as loss of more than 500 ml of blood during the first 24 hours:
       
        Normally, 200-600 ml of blood are lost before myometrial retraction plus strong uterine contractions stop flow.
        The majority of cases are associated with either an atonic uterus or placental remnants. The remainder are associated with laceration of the genital tract, rarely uterine rupture or blood coagulation defect.
        Treatment in situations where the placenta is still in the uterus is the combination of controlled cord traction with fundal pressure. If this fails, manual removal of the placenta under general anaesthesia is carried out.
       If the placenta has already been expelled, treatment includes massaging the uterus, intravenous (IV) ergometrine or syntocinon, or misoprostol, blood transfusion, correction of coagulation defects, and bimanual compression of the uterus; urgent transfer to theatre for surgery may be required.</p>
   <p> Secondary postpartum haemorrhage is abnormal bleeding after 24 hours up until six weeks postpartum:
        Usual causes are:
       <ul>
           <li>  Endometritis.</li>
           <li> Retained placental fragment and/or blood clots (usually detected by ultrasound)</li></ul>
        The uterus is often found to be bulky and tender with the cervix open.
            Initially, it is treated with removal of any clot visible within the os, plus antibiotics. Elective curettage may be required if bleeding persists despite this.

            <p>Puerperal pyrexia</p>

    <p>Puerperal pyrexia is defined as temperature 38°C or above during the first 14 days after delivery.
    Although this now occurs rarely in high-income countries, it remains an important cause of maternal death.
    Most cases are due to anaerobic streptococci that normally inhabit the vagina. Initially, they infect the placental bed and then spread either into the parametrium or via the uterine cavity to the Fallopian tubes and, occasionally, the pelvic peritoneum.
        Alternatively, there may be breast infection or urinary tract infection.</p>

            <p>Thromboembolism</p>

            <p> This occurs in about 1/1,000 pregnancies. It is 4-5 times more likely in pregnancy than in the non-pregnant woman. The puerperium is the time of highest risk (20-fold increased risk). It is more likely to occur in women who are overweight, over the age of 35 years or who have had a caesarean section.</p>
   <p> Thromboembolism is the leading cause of direct maternal mortality in the UK
       Treatment is with low molecular weight heparin (LMWH). This should be started immediately on suspicion of a thromboembolism (unless strongly contra-indicated), until the diagnosis is excluded.</p>
            
            <p> Deep vein thrombosis (DVT):</p>
            
       <p> Leg pain and swelling (usually unilateral). Calf muscles are tender and painful on firm palpation. There may be low abdominal pain or thigh pain and tenderness. Low-grade fever (less than 38oC) may occur.
        Clinical signs are unreliable (and D-dimer cannot be used in pregnancy and puerperium), so confirmation is needed with compression duplex ultrasound.
        Treatment is with LMWH which should be started immediately on suspicion of DVT.
           If the ultrasound is negative but DVT is still suspected, LMWH can be stopped but the ultrasound repeated on days 3 and 7.</p>
            
            <p> Pulmonary embolism (PE):</p>
       <p> Suspected PE is an emergency: 15% of patients with a PE in pregnancy will die, 2/3 of them within 30 minutes.
        Dyspnoea, haemoptysis and pleural pain; cyanosis may develop later. Massive PE may present with collapse.
        Friction rub may be heard in the chest.
        ECG should be performed. It will be abnormal in 41% but may suggest an alternative diagnosis, such as coronary heart disease.
        CXR should be performed. It will be abnormal in under 50% but may suggest an alternative diagnosis, such as pneumothorax.
        If DVT is also suspected, PE may be diagnosed and treatment started if DVT is confirmed on compression duplex ultrasound.
        If DVT is not suspected, a ventilation/perfusion (V/Q) scan or computerised tomography pulmonary angiogram (CTPA) should be performed.
        Treatment is with LMWH (IV unfractionated heparin bolus followed by infusion +/- thrombolysis for massive PE).
           Self-administered LMWH or oral warfarin is continued for at least three months. LMWH is associated with a significantly lower risk of post-thrombotic syndrome compared with warfarin.</p>

            <p> <h5 id="PN2"> Postnatal care</h5>

            <p>This is based on National Institute for Health and Care Excellence (NICE) guidance</p>

    <p>Women should be offered information to enable them to promote their own and their baby's health and well-being and to recognise and respond to problems.
    At the first postnatal contact, women should be advised of the signs and symptoms of haemorrhage, infection, thromboembolism and pre-eclampsia/eclampsia and the appropriate action to take.
    All maternity care providers should encourage breast-feeding.
    At each postnatal contact, women should be asked about their emotional well-being, what family and social support they have and their usual coping strategies for dealing with day-to-day matters.
    Women and their families/partners should be encouraged to tell their healthcare professional about any changes in mood, emotional state and behaviour that are outside of the woman's normal pattern.
    At each postnatal contact, parents should be offered information and advice to enable them to:
        <ul>
            <li> Assess their baby's general condition.</li>
            <li> Identify signs and symptoms of common health problems seen in babies.</li>
            <li> Contact a healthcare professional or emergency service if required.</li></ul>

            <p> <h5 id="PN3"> Maternal activity </h5>

    <p>The mother should start walking about as soon as possible, go to the toilet when necessary and rest when she needs to. She may prefer to stay in bed for the first 24 hours or longer if she has an extensive perineal repair.
    This is an important time for the woman to be encouraged to breast-feed and learn to care for her infant.
        Uterine contractions continue after birth and some women experience after-pains, particularly when breast-feeding. Analgesics may be required.</p>

            <p> <h5 id="PN4">Infant Feeding </h5>

    <p>Whether they chose to breast-feed or bottle-feed, women often need a lot of advice and support, especially with their first baby (but experienced mothers shouldn't be assumed to know everything and support and advice should always be available).
    Breast-feeding should be strongly encouraged (first-time mothers may need a lot of support and encouragement initially). Breast-feeding has many advantages, including:
        <ul>
            <li>It is free and available without preparation.</li>
            <li>Boosting the baby's immunity to infection, particularly respiratory tract infections and gastroenteritis, with a consequent reduction in hospitalisations.</li>
            <li>Reducing risk of cot death.</li>
            <li>Promoting bonding between the mother and her baby.</li>
            <li> Protection against later risks of being overweight and obese.</li>
            <li>Reduction in breast and ovarian cancer and diabetes in the mother.</li></ul>
    Initial breast engorgement may cause a lot of discomfort but is usually relieved by good bra support and analgesia.
    Women who are unable to breast-feed or prefer to bottle-feed also need support and advice, including around feeding routines and sterilising.

            <p> <h5 id="PN5">Contraception </h5>

   <p> Contraception is not necessary in the 21 days after childbirth.
    <p>All methods are suitable choices for both breast-feeding and non-breast-feeding women
        Combined hormonal contraceptive (CHC) methods (pill, vaginal ring or patch) should not be started before 21 days, due to the increased risk of thromboembolism in the immediate postpartum period. Previous concerns about hormonal effects on the quality and quantity of milk, passage of hormones to the infant and adverse effects on infant growth if CHC methods are used in breast-feeding women before six months postpartum have not been proven. This led to the UK Medical Eligibility Criteria (UKMEC) being changed in 2016[10].
        The copper intrauterine contraceptive device (IUCD) and the intrauterine system (IUS), unless fitted within 48 hours of delivery, should not be fitted before four weeks postpartum, due to the increased risk of expulsion.
        Sterilisation is usually delayed until at least six weeks post-delivery.
        Fertility awareness methods can be used from day 21 but should not be taught to new users until after periods have restarted.</p>
    <p>The lactational amenorrhoea method is 98% effective if
         <ul>   
             <li> There is complete amenorrhoea.</li>
             <li> The woman is fully or very nearly fully breast-feeding.</li>
             <li> The baby is no more than 6 months old.</li>
          </ul>
