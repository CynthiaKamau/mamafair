      <h2>PRE NATAL INFORMATION </h2>
      <h3>KEEPING WOMEN INFORMED</h3>

<p>Care should be centred on the pregnant woman; the aim should be to keep her fully informed on the progress of her pregnancy and to provide her with evidence-based information and support to make informed decisions.</p>

<p>At first contact with a health professional, she should be given:

    Information on where antenatal care will be offered and by whom, including choice of providers where available and information about antenatal screening.
    Information about folic acid supplementation.
    Lifestyle advice including:
        Food hygiene and safe eating in pregnancy.
        Smoking cessation.
        Advice about avoidance of alcohol and illicit drugs in pregnancy.
    Medication advice (review of safety of any current medication in pregnancy and avoidance of over-the-counter (OTC) medication which may not be used in pregnancy).</p>

<P>At booking, she should be given:

    Information about the development of the baby during pregnancy.
    The choice of attending antenatal classes.
    Written information about antenatal care - for example, the book "The Pregnancy Book" available from Health Departments.[2]Patients with loss of sight or hearing, learning difficulties or poor comprehension of English should have the information provided in a way that is understandable to them.
    Advice about exercise.
    Information and choice regarding the place of birth.
    Information regarding nutrition and diet.
    Information about breast-feeding.
    Further explanation of antenatal screening.
    The opportunity to discuss any mental health issues.</P>

<P>Before or at 36 weeks, she should be given:
<ul>
    <li> Information about breast-feeding.</li>
    <li> Information to prepare her for labour and birth (birth plan, pain relief options, how to recognise the onset of active labour).</li>
    <li> Information about care of the new baby and preparations needed.</li>
    <li>Information about routine procedures such as newborn screening and vitamin K prophylaxis.</li>
    <li>Advice about postnatal self-care, along with information about postnatal depression and "baby blues".</li>
    </ul>
            </P>
